package modelo.beans;

public class Percepciones {
 
		private int idPercepcion;
		private String nombre;
		private String descripcion;
		private int diasPagar;
		public int getIdPercepcion() {
			return idPercepcion;
		}
		public void setIdPercepcion(int idPercepcion) {
			this.idPercepcion = idPercepcion;
		}
		public String getNombre() {
			return nombre;
		}
		public void setNombre(String nombre) {
			this.nombre = nombre;
		}
		public String getDescripcion() {
			return descripcion;
		}
		public void setDescripcion(String descripcion) {
			this.descripcion = descripcion;
		}
		public int getDiasPagar() {
			return diasPagar;
		}
		public void setDiasPagar(int diasPagar) {
			this.diasPagar = diasPagar;
		}
		
		
}
